/**
 * An ice class, a ingredient
 * @author yklam2
 */

public class Ice extends Ingredient {

	/**
	 * Create an ice object of a specific <code>volume</code>.
	 * @param volume Volume of ice
	 */
	public Ice(int volume) {
		super(volume);
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	public String toString() {
		return "Ice";
	}

}
